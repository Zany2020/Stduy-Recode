int isPrime(int num)
{
	if (num % 2 == 0)
	{
		return 0;//假
	}

	for (int i = 3; i <= sqrt(num); i += 2)//sqrt指开平方
	{
		if (num % i == 0)
		{
			return 0;//假
		}
	}
	return 1;//真
}


int main()
{
	for (int num = 100; num <= 200; num++)
	{
		if (isPrime(num))
		{
			printf("%d ", num);
		}
	}
	printf("\n");
	return 0;
}